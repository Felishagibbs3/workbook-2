package com.pluralsight;

public class WhileLoop {

    public static void main(String[] args) {
        // set variables for integer
        int loop = 0;

        //Since 5 is greater than 0, this will loop until it reaches the number 5 and stop.
        while (loop < 5) {
            System.out.println("I Love Java");
            loop ++;
        }
    }
}
